package com.i4joy.akka.kok.monster.mercenary.templet;

public class TeamTemplet {
	private int level;
	private int teamNum;
	private int team2Num;

	public int getLevel() {
		return level;
	}

	public void setLevel(int level) {
		this.level = level;
	}

	public int getTeamNum() {
		return teamNum;
	}

	public void setTeamNum(int teamNum) {
		this.teamNum = teamNum;
	}

	public int getTeam2Num() {
		return team2Num;
	}

	public void setTeam2Num(int team2Num) {
		this.team2Num = team2Num;
	}

}
